    private void add_bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_bActionPerformed
        try {
            // TODO add your handling code here:
            int row = customer_table.getSelectedRow();
            int rowCount = tblmodel.getRowCount();
            barcode = (String) customer_table.getValueAt(row, 0);
            commodityName = (String) customer_table.getValueAt(row, 1);
            price = (String) customer_table.getValueAt(row, 2);
            number = (String) customer_table.getValueAt(row, 3);
            allprice = (String) customer_table.getValueAt(row, 4);
            result = 0;
            allprices.removeAll(allprices);
            for (int i =0; i<rowCount; i++) {
                allprices.add(Long.parseLong((String) customer_table.getValueAt(i, 4)));
            }
            for (long allPrice : allprices) {
                result = result + allPrice;
            }
            result_l.setText(result + "");
            st.executeUpdate("INSERT INTO `" + name_l.getText() + "` (barcode,commodityname,price,number,allprice) VALUES ('" + barcode + "','" + commodityName + "','" + price + "','" + number + "','" + allprice + "')");
            st.executeUpdate("SET @count = 0;");
            st.executeUpdate("UPDATE `" + name_l.getText() + "` SET id = @count:= @count + 1;");
            st.executeUpdate("UPDATE customers SET result = '" + result + "' WHERE customername LIKE '" + name_l.getText() + "'");
            add_l.setText("کالا اضافه شد");
            Timer t = new Timer (3000 , new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    add_l.setText("");
                }
            });
            t.start();
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_add_bActionPerformed

