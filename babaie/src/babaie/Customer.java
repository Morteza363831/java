    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            rs1 = st.executeQuery("SELECT COUNT(*) FROM `" + name_l.getText() + "`");
            if (rs1.next()) {
                range = rs1.getInt(1);
                String[][] values = new String[5*range][5];
                int num = 0;
                rs2 = st.executeQuery("SELECT * FROM `" + name_l.getText() + "`");
                while (num<5*range && rs2.next()) {
                    values[num][0] = rs2.getString("barcode");
                    values[num][1] = rs2.getString("commodityname");
                    values[num][2] = rs2.getString("price");
                    values[num][3] = rs2.getString("number");
                    values[num][4] = rs2.getString("allprice");
                    num++;
                }
                rs3 = st.executeQuery("SELECT * FROM customers WHERE customername LIKE '" + name_l.getText() + "'");
                if (rs3.next()) {
                    result = Long.parseLong(rs3.getString("result"));
                    result_l.setText(result + "");
                }
                // make rows in java table
                for (int i = 0 ; i<range ; i++) {
                    tblmodel.addRow(values[i]);
                }   
            }
        } catch (SQLException ex) {
            Logger.getLogger(Commoditys.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

